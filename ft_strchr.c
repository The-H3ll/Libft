#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)s + i);
		if (s[i + 1] == c)
			return ((char *)s + i + 1);
		i++;
	}
	return (NULL);
}
